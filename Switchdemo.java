package Switchdemo;
public class Switchdemo
{
public static void main(String[] args)
{
	int day=5;
	 switch(day) 
	 {
	 case 21:System.out.println("Monday");
	       break;
	 case 22:System.out.println("Tuesday");
	       break;
	 case 23:System.out.println("Wednesday");
	       break;
	 case 24:System.out.println("Thursday");
	       break;
	 case 25:System.out.println("Friday");
	       break;
	 case 26:System.out.println("Saturday");
	      break;
	 case 7:System.out.println("Sunday");
	      break;
	 default:System.out.println("In valid1");
	 }
}
}

			
	


